package com.example.procreator.model;

public class Personnage {
    private String prenom;
    private String nom;
    private String sexe;
    private int age;
    private String fonction;

    public String getFonction() {
        return fonction;
    }

    public String getSexe() {
        return sexe;
    }

    public String getNom() {
        return nom;
    }

    public String getPrenom() {
        return prenom;
    }

    public int getAge() {
        return age;
    }

}
